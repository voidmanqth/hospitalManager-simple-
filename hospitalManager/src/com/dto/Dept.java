package com.dto;

public class Dept {
	private String name;//部门名称
	private String kind;//部门类别

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getKind() {
		return kind;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}

}
